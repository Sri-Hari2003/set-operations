
A = {0, 2, 4, 6, 8}
B = {1, 2, 3, 4, 5}

union_result = A.union(B)
intersection_result = A.intersection(B)
difference_result = A.difference(B)
symmetric_difference_result = A.symmetric_difference(B)

print("Union of A and B is", union_result)
print("Intersection of A and B is", intersection_result)
print("Difference of A and B is", difference_result)
print("Symmetric difference of A and B is", symmetric_difference_result)
